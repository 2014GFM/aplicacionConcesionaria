    public void llenarListaDeVendedores() throws Exception{
        // Obtengo una conexion
        Connection unaConexion = AdministradorDeConexiones.obtenerConexion();
        
        // Armo la query
        String unaConsulta = "select * from vendedores";
        
        // Creo un objeto de tipo Statement que ejecutara la sentencia
        Statement unaSentencia = unaConexion.createStatement();
        
        // Ejecuto la senetencia y almaceno el resultado en un objeto de tipo ResulSet
        ResultSet unResultado = unaSentencia.executeQuery(unaConsulta);
        
        // Recorro el resultado y lo imprimo en consola
        while(unResultado.next()){
            int idvendedores = unResultado.getInt("idvendedores");
            String nombre = unResultado.getString("nombre");
            String apellido = unResultado.getString("apellido");
            int numeroDocumento = unResultado.getInt("numeroDocumento");
            int cantAutosVendidos = unResultado.getInt("cantAutosVendidos");
            
            Vendedor unVendedor = new Vendedor(cantAutosVendidos, idvendedores, nombre, apellido, numeroDocumento);
            list_model.agregarPersona(unVendedor);
        }
        // Cierro la sentencia
        unaSentencia.close();
        unaConexion.close();
        
        list_listaVendedores.setModel(list_model);
    }
    
