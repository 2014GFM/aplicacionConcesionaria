    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        //Tomo valor de caja de texto
        String nombre = txt_nombre.getText();
        String apellido = txt_apellido.getText();
        String documento = txt_documento.getText();
        String autosVendidos = txt_autosVendidos.getText();
        
        //Valido que no este vacio
        if(nombre.isEmpty()){
            // Si nombre esta vacio muestro caja de dialogo
            JOptionPane.showMessageDialog(null, "El nombre no puede estar vacio", "Alerta", JOptionPane.WARNING_MESSAGE);
            // Pongo el foco en nombre
            txt_nombre.requestFocus();
        }
        else if(apellido.isEmpty()){
            JOptionPane.showMessageDialog(null, "El apellido no puede estar vacio", "Alerta", JOptionPane.WARNING_MESSAGE);
            txt_apellido.requestFocus();
        }
        else if(documento.isEmpty()){
            JOptionPane.showMessageDialog(null, "El documento no puede estar vacio", "Alerta", JOptionPane.WARNING_MESSAGE);
            txt_documento.requestFocus();
        }
        //Valido que dato ingresado sea numero
        else if(!documento.matches("[0-9]*")){
            // Si documento no es numero muestro caja de dialogo
            JOptionPane.showMessageDialog(null, "El documento debe ser un numero entero", "Alerta", JOptionPane.WARNING_MESSAGE);
            // Pongo foco en documento
            txt_documento.requestFocus();
            // Limpio caja de texto
            txt_documento.setText(null);           
        }
        else if(autosVendidos.isEmpty()){
            JOptionPane.showMessageDialog(null, "La cantidad de autos vendidos no puede estar vacio", "Alerta", JOptionPane.WARNING_MESSAGE);
            txt_autosVendidos.requestFocus();
        }
        else if(!autosVendidos.matches("[0-9]*")){
            JOptionPane.showMessageDialog(null, "La cant. de autos vendidos debe ser un numero entero", "Alerta", JOptionPane.WARNING_MESSAGE);
            txt_autosVendidos.requestFocus();
            txt_autosVendidos.setText(null);           
        }
        else{
            //Si todo esta completo: guardo, limpio cajas de texto, habilito boton nuevo y desabilito boton guardar
            JOptionPane.showMessageDialog(null, "El vendedor ha sido guardado");
            txt_nombre.setText(null);
            txt_apellido.setText(null);
            txt_documento.setText(null);
            txt_autosVendidos.setText(null);
            
            btn_nuevo.setEnabled(true);
            btn_guardar.setEnabled(false);
            
            // Parseo presupuesto y documento
            int cantAutosVendidos = Integer.parseInt(autosVendidos);
            int numeroDocumento = Integer.parseInt(documento);
            
            // Instancio nuevo comprador
            Vendedor p = new Vendedor();
            p.setNombre(nombre);
            p.setApellido(apellido);
            p.setNumeroDocumento(numeroDocumento);
            p.setCantAutosVendidos(cantAutosVendidos);
            try {
                int idvendedores = 0;
                list_model.agregarPersonaDB(p, idvendedores, nombre, apellido, numeroDocumento, cantAutosVendidos);
            } catch (Exception ex) {
                Logger.getLogger(vendedoresVentana.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // Elimino auto
        int selection = list_listaVendedores.getSelectedIndex();
        int resp = JOptionPane.showConfirmDialog(null, "Desea eliminar el Vendedor seleccionado?", "Elija una opcion", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (selection!=-1 && resp == 0) {
            try {
                Vendedor p = list_model.getPersona(selection);
                list_model.eliminarPersona(p, selection);
                btn_eliminar.setEnabled(false);
                JOptionPane.showMessageDialog(null, "El Vendedor ha sido eliminado");
                txt_nombre.setText(null);
                txt_apellido.setText(null);
                txt_documento.setText(null);
                txt_autosVendidos.setText(null);
                btn_guardar.setEnabled(false);
            } catch (Exception ex) {
                Logger.getLogger(vendedoresVentana.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

