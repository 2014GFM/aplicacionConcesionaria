    private void list_listaVendedoresValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_list_listaVendedoresValueChanged
        // Selecciono y muestro datos de auto guardado
        int selection = list_listaVendedores.getSelectedIndex();
        if (selection!=-1) {            
            Vendedor p = list_model.getPersona(selection);
            txt_nombre.setText(p.getNombre());
            txt_apellido.setText(p.getApellido());
            txt_documento.setText(Integer.toString(p.getNumeroDocumento()));
            txt_autosVendidos.setText(Integer.toString(p.getCantAutosVendidos()));
            
            // Habilito boton eliminar
            btn_eliminar.setEnabled(true);
        }
    }//GEN-LAST:event_list_listaVendedoresValueChanged


